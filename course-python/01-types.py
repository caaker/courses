# an integer - <class 'int'>

i = 4
print(type(i))

# a float - <class 'float'>
f = 4.0
print(type(f))

# a string - <class 'str'>
s = "test"
print(type(s))

# an integer - <class 'int'>
s = (4)
print(type(s))

# a tuple - <class 'tuple'>, a tuple does not have size or shape, just length, it is immutable
t = (4,)
print(type(t))
# print(t)
# print(len(t))

# a dynamic array - <class 'list'>
print(type([0]))

# a hash table - <class 'dict'>
print(type({'a': 0}))

